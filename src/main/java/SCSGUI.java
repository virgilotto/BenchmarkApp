    private void benchButActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        Test test = new Test();
        MyMatrix matrix = new MyMatrix();
        Pi pi = new Pi();

        long timeAdd = test.test("Addition (double)", (Double a, Double b) -> {
            return a + b;
        }, 483902.7743, 42347.775, 10_000_000);
        jTextField3.setText(Long.toString(timeAdd));

        long timeSub = test.test("Subtraction (double)", (Double a, Double b) -> {
            return a - b;
        }, 483902.7743, 42347.775, 10_000_000);
        jTextField4.setText(Long.toString(timeSub));

        long timeMul = test.test("Multiplication (double)", (Double a, Double b) -> {
            return a * b;
        }, 483902.7743, 42347.775, 1_000_000);
        jTextField5.setText(Long.toString(timeMul));

        long timeDiv = test.test("Division (double)", (Double a, Double b) -> {
            return a / b;
        }, 483902.7743, 42347.775, 1_000_000);
        jTextField6.setText(Long.toString(timeDiv));

        long timeLog10 = test.test("Log10", (Double a, Double b) -> {
            return Math.log10(a);
        }, 483902.7743, 42347.775, 1_000_000);
        jTextField7.setText(Long.toString(timeLog10));

        long timeLogE = test.test("LogE", (Double a, Double b) -> {
            return Math.log(a);
        }, 483902.7743, 42347.775, 1_000_000);
        jTextField8.setText(Long.toString(timeLogE));

        long timePow = test.test("Power", (Double a, Double b) -> {
            return Math.pow(a, b);
        }, 483902.7743, 12, 100_000);
        jTextField9.setText(Long.toString(timePow));

        Double[][] A = { { 483902.7743, 42347.775 }, { 483902.7743, 42347.775 } };
        Double[][] B = { { 483902.7743, 42347.775 }, { 483902.7743, 42347.775 } };
        long timeMatrix = matrix.testFunct(A, B);
        jTextField12.setText(Long.toString(timeMatrix));

        long timePi = pi.pi(Integer.parseInt(jTextField10.getText()));
        jTextField11.setText(Long.toString(timePi));

        long total = timeAdd + timeSub + timeMul + timeDiv +
                timeLog10 + timeLogE + + timePow + timePi + timeMatrix;

        jTextField13.setText(Long.toString(total));

        jProgressBar1.setValue(100);
    }


